def firstBadVersion(n):
    """
    Binary search for first bad version
    
    Key: All versions after first bad are also bad.
    This creates a sorted pattern: [False, False, True, True...]
    
    Example: n = 5, bad = 4
    â”€â”€â”€â”€â”€â”€â”€â”€
    Versions: [1, 2, 3, 4, 5]
    Status:   [G, G, G, B, B]
    
    left=1, right=5, mid=3
    isBadVersion(3)=False â†’ Search right
    
    left=4, right=5, mid=4
    isBadVersion(4)=True â†’ Search left (keep mid)
    
    left=4, right=4 â†’ Found! Return 4
    """
    left, right = 1, n
    
    while left < right:
        mid = left + (right - left) // 2  # Avoid overflow
        
        if isBadVersion(mid):
            # Mid is bad, first bad might be mid or earlier
            right = mid
        else:
            # Mid is good, first bad is after mid
            left = mid + 1
    
    return left


