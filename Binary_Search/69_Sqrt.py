def mySqrt_bruteforce(x):
    """
    Try every number from 0 to x
    
    Time: O(âˆšx) - check up to sqrt(x)
    Space: O(1)
    """
    if x < 2:
        return x
    
    for i in range(x):
        if i * i > x:
            return i - 1
    
    return x


