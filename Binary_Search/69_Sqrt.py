def mySqrt(x):
    """
    Binary search for sqrt
    
    Search range: [0, x]
    
    Example: x = 8
    â”€â”€â”€â”€â”€â”€â”€â”€
    left=0, right=8, mid=4
    4*4=16 > 8 â†’ Too big, search left
    
    left=0, right=3, mid=1
    1*1=1 < 8 â†’ Too small, search right
    
    left=2, right=3, mid=2
    2*2=4 < 8 â†’ Too small, search right
    
    left=3, right=3, mid=3
    3*3=9 > 8 â†’ Too big, search left
    
    left=3, right=2 â†’ Done! Return 2
    """
    if x < 2:
        return x
    
    left, right = 0, x
    
    while left <= right:
        mid = (left + right) // 2
        square = mid * mid
        
        if square == x:
            return mid
        elif square < x:
            left = mid + 1
        else:
            right = mid - 1
    
    return right  # Return floor value


