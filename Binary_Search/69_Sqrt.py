def mySqrt_newton(x):
    """
    Newton-Raphson method for faster convergence
    
    Formula: x_new = (x_old + n/x_old) / 2
    
    Time: O(log x)
    Space: O(1)
    """
    if x < 2:
        return x
    
    # Start with x/2 as guess
    guess = x
    while guess * guess > x:
        guess = (guess + x // guess) // 2
    
    return guess


