def findMin(nums):
    """
    Binary search for minimum
    
    Key: Compare mid with right
    
    Example: [4,5,6,7,0,1,2]
    â”€â”€â”€â”€â”€â”€â”€â”€
    left=0, right=6, mid=3
    nums[mid]=7 > nums[right]=2 â†’ Min in RIGHT half
    
    left=4, right=6, mid=5
    nums[mid]=1 < nums[right]=2 â†’ Min in LEFT half (including mid)
    
    left=4, right=5, mid=4
    nums[mid]=0 < nums[right]=1 â†’ Min in LEFT half
    
    left=4, right=4 â†’ FOUND! Return nums[4]=0
    """
    left, right = 0, len(nums) - 1
    
    while left < right:
        mid = (left + right) // 2
        
        # Min is in right half
        if nums[mid] > nums[right]:
            left = mid + 1
        # Min is in left half (including mid)
        else:
            right = mid
    
    return nums[left]


