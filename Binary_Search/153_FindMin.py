def findMin_verbose(nums):
    """
    More explicit comparisons
    """
    left, right = 0, len(nums) - 1
    
    # Already sorted (no rotation)
    if nums[left] < nums[right]:
        return nums[left]
    
    while left < right:
        mid = (left + right) // 2
        
        # Check if mid is pivot
        if nums[mid] > nums[mid + 1]:
            return nums[mid + 1]
        if nums[mid] < nums[mid - 1]:
            return nums[mid]
        
        # Decide which half
        if nums[mid] > nums[0]:
            left = mid + 1
        else:
            right = mid
    
    return nums[left]


