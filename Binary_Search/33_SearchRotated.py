def search(nums, target):
    """
    Binary search on rotated array
    
    Key Insight: One half is always sorted!
    
    Example: [4,5,6,7,0,1,2], target = 0
    â”€â”€â”€â”€â”€â”€â”€â”€
    left=0, right=6, mid=3
    nums[mid]=7 > nums[left]=4 â†’ LEFT half sorted
    target=0 not in [4,7] â†’ search RIGHT
    
    left=4, right=6, mid=5
    nums[mid]=1 < nums[right]=2 â†’ RIGHT half sorted
    target=0 not in [1,2] â†’ search LEFT
    
    left=4, right=4, mid=4
    nums[mid]=0 â†’ FOUND! Return 4
    """
    left, right = 0, len(nums) - 1
    
    while left <= right:
        mid = (left + right) // 2
        
        if nums[mid] == target:
            return mid
        
        # Left half is sorted
        if nums[left] <= nums[mid]:
            # Target in sorted left half?
            if nums[left] <= target < nums[mid]:
                right = mid - 1
            else:
                left = mid + 1
        # Right half is sorted
        else:
            # Target in sorted right half?
            if nums[mid] < target <= nums[right]:
                left = mid + 1
            else:
                right = mid - 1
    
    return -1


